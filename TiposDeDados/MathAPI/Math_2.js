//abs: converte sinal do número para positivo
//ceil: arredonda o número para cima
//floor: arredonda o número para baixo
//round: arredonda o número para cima se 5 e 9 ou para baixo se 0 e 4
//sign: retorna 1 para positivo e -1 para negativo
//trunc: elimina a parte decimal do número, tornando-o inteiro.

Math.abs(10);
Math.abs(-10);
Math.ceil(1.1);
Math.ceil(-1.1);
Math.floor(9.9);
Math.floor(-9.9);
Math.round(4.5);
Math.round(-4.5);
Math.sign(5);
Math.sign(-5);
Math.trunc(2.3);
Math.trunc(-2.3);
